from enum import Enum


class Align(Enum):
    """
    Enumeration representing text alignment options.
    """

    LEFT = 'Left'
    """
    Align text to the left side.
    """
    CENTER = 'Center'
    """
    Center the text horizontally.
    """
    RIGHT = 'Right'
    """
    Align text to the right side.
    """
